#[test]
fn first_run_seeding_is_idempotent() {
    let store = temp_store("demo-seed");

    let first = eo9(&store, &["-c", "help"]);
    assert_eq!(first.code, 0, "stderr: {}", first.stderr);
    assert!(
        first.stderr.contains("seeded"),
        "first run should seed: {}",
        first.stderr
    );

    let names = |run: &Run| -> Vec<String> {
        run.stdout
            .lines()
            .skip_while(|line| !line.starts_with("names"))
            .take_while(|line| line.starts_with("names") || line.starts_with("  "))
            .map(str::to_owned)
            .collect()
    };
    let listed = eo9(&store, &["store", "ls"]);
    assert_eq!(listed.code, 0);
    let seeded_names = names(&listed);
    for expected in [
        "hello ",
        "eosh ",
        "cruncher ",
        "entropy.seeded ",
        "fs.memfs ",
    ] {
        assert!(
            seeded_names
                .iter()
                .any(|line| line.trim_start().starts_with(expected)),
            "expected {expected:?} among the seeded names: {seeded_names:?}"
        );
    }

    // A second shell start must not reseed or change any binding.
    let second = eo9(&store, &["-c", "help"]);
    assert_eq!(second.code, 0, "stderr: {}", second.stderr);
    assert!(
        !second.stderr.contains("seeded"),
        "second run must not reseed: {}",
        second.stderr
    );
    let relisted = eo9(&store, &["store", "ls"]);
    assert_eq!(names(&relisted), seeded_names);
}

